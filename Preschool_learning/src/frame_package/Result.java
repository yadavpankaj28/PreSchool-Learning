    public Result(Vector al) {
        this();
        this.al = al;
        JTable jt = jTable1;
        jt.setModel(new DefaultTableModel(al, title));
        setVisible(true);
        setSize(650, 350);
        setLocation(250, 100);
        Iterator it = al.iterator();
        while (it.hasNext()) {            
            Vector v = (Vector)it.next();
            String ans = (String)v.get(1);   
            String cans = (String)v.get(2);
            if(ans.equals(cans)){
                correct++;
            }else{
                incorrect++;
            }
            System.out.println(ans +"  " + cans);
            
        }
        System.out.println(correct+"  " + incorrect);
        corret_ans.setText(correct+"");
        incorrect_ans.setText(incorrect+"");
        total.setText(correct+"");
    }

    
